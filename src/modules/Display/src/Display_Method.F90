FUNCTION Int2Str(I)
  ! Define intent of dummy arguments
  INTEGER(I4B), INTENT(IN) :: I
  CHARACTER(15) :: Int2Str
  ! Define internal variables
  CHARACTER(15) :: Str
  WRITE (Str, "(I15)") I
  Int2Str = TRIM(ADJUSTL(Str))
END FUNCTION Int2Str

