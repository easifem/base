!> author: Vikas Sharma, Ph. D.
! date: 21 Sept 2021
! summary: This subroutine Displays the string
!
!# Introduction
! This routine displays a string
!
!## usage
!```fortran
! CALL Display( msg="hello world", unitno=stdout )
!```

SUBROUTINE Display_Str(msg, unitno, advance)
  ! Dummt arguments
  CHARACTER(*), INTENT(IN) :: msg
  !! input message
  INTEGER(I4B), OPTIONAL, INTENT(IN) :: unitno
  !! unit no
  LOGICAL(LGT), OPTIONAL, INTENT(IN) :: advance
  ! Internal variables
  INTEGER(I4B) :: i
  CHARACTER(:), ALLOCATABLE :: advance0
  LOGICAL(LGT) :: bool1

  CALL setDefaultSettings

  i = stdout; IF (PRESENT(unitno)) i = unitno
  bool1 = .TRUE.; IF (PRESENT(advance)) bool1 = advance

  IF (bool1) THEN
    advance0 = "YES"
  ELSE
    advance0 = "NO"
  END IF

#ifdef COLOR_DISP
  CALL DISP(title="", &
    & x=TRIM(colorize(msg, color_fg=COLOR_FG, color_bg=COLOR_BG, &
    & style=COLOR_STYLE)), &
    & FMT='a', unit=i, style="left",  &
    & advance=advance0)
#else
  CALL DISP(title="", x=msg, FMT='a', unit=i, style="left",  &
    & advance=advance0)
#endif
END SUBROUTINE Display_Str

