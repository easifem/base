!> author: Vikas Sharma, Ph. D.
! date: 21 Sept 2021
! summary: This subroutine display a scalar logical variable
!
!# Introduction
!
! This subroutine display a scalar logical variable
!
!## Usage
!
!```fortran
!  call display( val=.TRUE., msg="var=", unitno=stdout)
!```

SUBROUTINE Display_Logical(val, msg, unitNo, advance)
  ! Define intent of dummy variables
  LOGICAL(LGT), INTENT(IN) :: val
  CHARACTER(*), INTENT(IN) :: msg
  INTEGER(I4B), INTENT(IN), OPTIONAL :: unitNo
  CHARACTER(*), OPTIONAL, INTENT(IN) :: advance

  ! Internal variables
  CHARACTER(:), ALLOCATABLE :: advance0
  LOGICAL(LGT) :: bool1

  advance0 = "YES"; IF (PRESENT(advance)) advance0 = TRIM(advance)

  SELECT CASE (TRIM(advance0))
  CASE ("YES")
    bool1 = .TRUE.
  CASE ("NO")
    bool1 = .FALSE.
  CASE default
    bool1 = .TRUE.
  END SELECT

  IF (val) THEN
    CALL Display_Str(msg=TRIM(msg)//" TRUE",  &
      & unitNo=unitNo, advance=bool1)
  ELSE
    CALL Display_Str(msg=TRIM(msg)//" FALSE",  &
      & unitNo=unitNo, advance=bool1)
  END IF
END SUBROUTINE Display_Logical

