! This subroutine prints the time stamp

SUBROUTINE TIMESTAMP()
  ! Define Intent of dummy Variable
  CHARACTER(8) :: ampm
  INTEGER(I4B) :: d
  INTEGER(I4B) :: h
  INTEGER(I4B) :: m
  INTEGER(I4B) :: mm
  CHARACTER(9), PARAMETER, DIMENSION(12) :: month = (/ &
                         'January  ', 'February ', 'March    ', 'April    ', &
                         'May      ', 'June     ', 'July     ', 'August   ', &
                          'September', 'October  ', 'November ', 'December '/)
  INTEGER(I4B) :: n
  INTEGER(I4B) :: s
  INTEGER(I4B) :: values(8)
  INTEGER(I4B) :: y

  CALL DATE_AND_TIME(values=values)

  y = values(1)
  m = values(2)
  d = values(3)
  h = values(5)
  n = values(6)
  s = values(7)
  mm = values(8)

  IF (h < 12) THEN
    ampm = 'AM'
  ELSE IF (h == 12) THEN
    IF (n == 0 .AND. s == 0) THEN
      ampm = 'Noon'
    ELSE
      ampm = 'PM'
    END IF
  ELSE
    h = h - 12
    IF (h < 12) THEN
      ampm = 'PM'
    ELSE IF (h == 12) THEN
      IF (n == 0 .AND. s == 0) THEN
        ampm = 'Midnight'
      ELSE
        ampm = 'AM'
      END IF
    END IF
  END IF

  WRITE (*, '(8x, i2,1x,a,1x,i4,2x,i2,a1,i2.2,a1,i2.2,a1,i3.3,1x,a)') &
    d, TRIM(month(m)), y, h, ':', n, ':', s, '.', mm, TRIM(ampm)

END SUBROUTINE TIMESTAMP
